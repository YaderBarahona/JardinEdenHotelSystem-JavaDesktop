    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int _fila = jTable1.getSelectedRow();
        if (fila != -1) {
            int idRoom = Integer.parseInt(jTable1.getValueAt(_fila, 0).toString());
            int resp = JOptionPane.showConfirmDialog(rootPane,
                    "esta seguro de eliminar la habitación ?" + idRoom, "Eliminar",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (resp == JOptionPane.YES_OPTION) {
                manageRooms.conectarBD();
                if (manageRooms.eliminarRoom(idRoom)) {
                    JOptionPane.showMessageDialog(rootPane, "Habitación eliminado exitosamente", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
                    limpiar_campos();
                    listarHabitaciones();

                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error al eliminar la habitación", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione la habitación que desea eliminar", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

