    private boolean BuscarCliente() {
        String codigo = txtID.getText();
        boolean encontrado = false;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            if (codigo.equals(jTable1.getValueAt(i, 0))) {
                encontrado = true;
                fila = i;
                jTable1.changeSelection(i, 1, false, false);
            }
        }
        return encontrado;
    }

